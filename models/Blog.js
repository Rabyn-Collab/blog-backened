import mongoose from "mongoose";

export const categories = ['Tech', 'Lifestyle', 'Travel', 'Food', 'Start up', 'Other'];

const blogSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  subtitle: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  category: {
    type: String,
    enum: categories,
    required: true,
  },
  image: {
    type: String,
    required: true
  },
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  }
}, {
  timestamps: true
});

const Blog = mongoose.model("Blog", blogSchema);

export default Blog;